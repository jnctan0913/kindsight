export const dictionaries: Record<Locale, Record<StringKey, string>> = {
  en,
  zh,
};

export function format(
  template: string,
  vars?: Record<string, string | number>,
): string {
  let out = template;
  if (vars) {
    for (const [name, value] of Object.entries(vars)) {
      out = out.replaceAll(`{${name}}`, String(value));
    }
  }
  return out;
}
