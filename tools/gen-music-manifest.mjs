// Scans public/music/{lobby,writing,wrapup} for audio files and writes
// src/lib/musicManifest.ts. Static export can't read a directory at runtime, so
// this runs on prebuild/predev. Drop a track in a folder, rebuild, done.
import {readdirSync, writeFileSync, existsSync} from 'node:fs';
import {join, dirname} from 'node:path';
import {fileURLToPath} from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const phases = ['lobby', 'writing', 'wrapup'];
const AUDIO = /\.(mp3|ogg|m4a|aac|wav)$/i;

const tracks = {};
for (const phase of phases) {
  const dir = join(root, 'public/music', phase);
  const files = existsSync(dir)
    ? readdirSync(dir).filter((f) => AUDIO.test(f)).sort()
    : [];
  tracks[phase] = files.map((f) => `/music/${phase}/${f}`);
}

const body = `// AUTO-GENERATED by tools/gen-music-manifest.mjs. Do not edit by hand.
export type MusicPhase = 'lobby' | 'writing' | 'wrapup';

// Paths are public-relative; wrap with asset() for basePath at use.
export const MUSIC_TRACKS: Record<MusicPhase, string[]> = ${JSON.stringify(tracks, null, 2)};

export const hasAnyMusic = ${Object.values(tracks).some((a) => a.length > 0)};
`;

writeFileSync(join(root, 'src/lib/musicManifest.ts'), body);
const total = Object.values(tracks).reduce((n, a) => n + a.length, 0);
console.log(`music manifest: ${total} track(s) [${phases.map((p) => `${p}:${tracks[p].length}`).join(', ')}]`);
